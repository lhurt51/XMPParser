#include "libft.h"

int	ft_atoi(const char *c)
{
	int i;
	int	sign;
	int	ans;

	i = 0;
	sign = 1;
	ans = 0;
	while (c[i] == ' ' || c[i] == '\n' || c[i] == '\t'
		|| c[i] == '\r' || c[i] == '\v' || c[i] == '\f')
		i++;
	if (c[i] == '-' || c[i] == '+')
	{
		if (c[i] == '-')
			sign *= -1;
		i++;
	}
	while (ft_isdigit(c[i]))
	{
		ans = (ans * 10) + (c[i] - 48);
		i++;
	}
	return (ans * sign);
}
